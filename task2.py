# Запит слова у користувача
word = input("Введіть слово кирилицею: ")

# Перевірка на наявність латинських літер замість кириличних
if "i" in word or "a" in word or "o" in word:
    print("Слово містить підміну літер на латинські.")
else:
    print("Слово не містить підміну літер на латинські.")
